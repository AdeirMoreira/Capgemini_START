package tipos.de.dados;

public class TiposDeDados {

    public static void main(String[] args) {
        int numero = 10;
        float decimalCurto = 0.5f;
        double decimalLongo = 3.14159265;
        char caractere = 'a';
        String frase = "aprendendo java";
        
        System.out.println("Esee é o numero: " + numero);
        System.out.println("Esee é o float: " + decimalCurto);
        System.out.println("Esee é o double: " + decimalLongo);
        System.out.println("Esee é o caractere: " + caractere);
        System.out.println("Esee é a string: " + frase);
    }
}
